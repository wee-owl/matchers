import sortHeroes from '../app';

const array = [
  { name: 'мечник', health: 10 },
  { name: 'маг', health: 100 },
  { name: 'лучник', health: 80 },
];

const result = [
  { name: 'маг', health: 100 },
  { name: 'лучник', health: 80 },
  { name: 'мечник', health: 10 },
];

test('sort ascending', () => {
  expect(sortHeroes(array)).toEqual(result);
});
